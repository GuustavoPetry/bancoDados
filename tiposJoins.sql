Tipos de JOIN:
- INNER JOIN -> Retorna dados que tenham correspondência em ambas as tabelas

- LEFT JOIN -> Retorna todos os dados da tabela da esquerda e os que tenham correspondência na tabela da direita
    LEFT JOIN tabela_esquerda ON tabela_esquerda.relacao = tabela_direita.id

- RIGHT JOIN -> Retorna todos os registros da tabela da direita e todos que tenham correspondência na tabela da esquerda (uso menos comum)

